# Задайте число. Составьте список чисел Фибоначчи,
# в том числе для отрицательных индексов.
# # Пример:
# для k = 8 список будет выглядеть так:
# [-21 ,13, -8, 5, −3, 2, −1, 1, 0, 1, 1, 2, 3, 5, 8, 13, 21]
# Негафибоначчи

a = int(input())
c1 = 1
c2 = -1
c = [c1,c2]
for i in range(a-2):
    c3 = c1-c2
    c.append(c3)
    c1 = c2
    c2 = c3
c = c[::-1]

b1 = 0
b2 = 1
c.append(b1)
c.append(b2)
for i in range(a-1):
    b3 = b1+b2
    c.append(b3)
    b1 = b2
    b2 = b3
print(c)